# BASIC PYTHON TUPLE
print("=== TUPLE EXAMPLES ===")

# 1. Create a tuple
fruits = ("apple", "banana", "mango")
print("Fruits:", fruits)

# 2. Access items
print("First fruit:", fruits[0])
print("Second fruit:", fruits[1])

# 3. Length of tuple
print("Length:", len(fruits))

# 4. Tuple with different data types
data = (10, "hello", True, 5.5)
print("Mixed tuple:", data)

# 5. Single item tuple
one_item = ("apple",)
print("Single item tuple:", one_item)