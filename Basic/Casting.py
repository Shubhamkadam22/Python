# BASIC PYTHON CASTING EXAMPLES


# 1. int() - Text/Float to Integer
x = "25"           
y = int(x)         
print("int('25') =", y, type(y))

z = 85.7
w = int(z)         
print("int(85.7) =", w)

# 2. float() - Text/Int to Float
a = "85.5"         
b = float(a)       
print("float('85.5') =", b)

c = 25
d = float(c)       
print("float(25) =", d)

# 3. str() - Number to Text
age = 25
text_age = str(age)  
print("str(25) =", text_age + " years")