# BASIC PYTHON DATA TYPES
# Python figures out type automatically

# 1. Integer (whole numbers)
age = 25
print("Integer:", age, type(age))  # <class 'int'>

# 2. Float (decimals)
marks = 85.5
print("Float:", marks, type(marks))  # <class 'float'>

# 3. String (text)
name = "Shubham"
city = 'Sholapur'
print("String:", name, type(name))  # <class 'str'>

# 4. Boolean (True/False)
is_student = True
print("Boolean:", is_student, type(is_student))  # <class 'bool'>

# 5. None (empty)
nothing = None
print("None:", nothing, type(nothing))  # <class 'NoneType'>

print("\n--- All types together ---")
print(age, marks, name, is_student)