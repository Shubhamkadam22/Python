# BASIC PYTHON VARIABLES
# Just assign: name = value

# 1. Numbers
age = 25
marks = 85.5
print("Age:", age)
print("Marks:", marks)

# 2. Text (strings)
name = "Shubham"
city = "Sholapur"
print("Name:", name)
print("City:", city)

# 3. True/False
is_student = True
print("Student?", is_student)

# 4. Multiple at once
a, b, c = 1, 2, 3
print(a, b, c)

# 5. Change value
x = 10
x = "ten"  # Now text!
print(x)